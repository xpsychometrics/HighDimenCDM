#' International Personality Item Pool (IPIP) data
#'
#' The IPIP-NEO-120 data are based on the public-domain personality inventory
#' developed from the International Personality Item Pool (IPIP; Johnson, 2014).
#'
#' The data consist of responses from 4271 participants to 120 dichotomized items
#' and the corresponding Q-matrix relating the 120 items to 30 personality facets.
#'
#' @format A list of responses and Q-matrix with components:
#' \describe{
#' \item{\code{dat}}{Responses of 4271 participants to 120 items.}
#' \item{\code{Q}}{The \eqn{120 \times 30} Q-matrix.}
#' }
#'
#' @author Yuxuan Mei, The University of Minnesota, \email{mei00060@umn.edu}
#'
#' @references
#'
#' Johnson, J. A. (2014). Measuring thirty facets of the five factor model with a 120-item  public domain inventory: Development of the ipip-neo-120. Journal of Research in  Personality, 51, 78–89.
